import java.util.Scanner;

public class demo5 {
    public static void main(String[] args) {
        //B2005 字符三角形
        Scanner scanner = new Scanner(System.in);
        String x = scanner.next();
        System.out.println("  "+x);
        System.out.println(" "+x+x+x);
        System.out.println(x+x+x+x+x);
    }
}
